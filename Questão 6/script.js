// Imprimir os números um abaixo do outro
console.log("Números um abaixo do outro:");
for (let i = 1; i <= 20; i++) {
    console.log(i);
}

// Imprimir os números um ao lado do outro
console.log("\nNúmeros um ao lado do outro:");
let numerosLadoALado = "";
for (let i = 1; i <= 20; i++) {
    numerosLadoALado += i + " ";
}
console.log(numerosLadoALado);
